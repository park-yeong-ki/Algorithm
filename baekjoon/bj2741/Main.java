package bj2741;

import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        //Scanner 선언
        Scanner sc = new Scanner(System.in);

        //N 입력
        int N = sc.nextInt();

        //출력
        for (int i = 1; i <= N; i++) {
            System.out.println(i);
        }
    }
}
